public class LogicOperators {
    public static void main(String[] args) {
        boolean b = true;
        int k = 5;
        if (k > 5) {
            System.out.println(">");
        }
        if (k < 5) {
            System.out.println("<");
        }
        if (k >= 5) {
            System.out.println(">=");
        }
        if (k <= 5) {
            System.out.println("<=");
        }
        if (k == 5) {
            System.out.println("==");
        }
        if (k != 5) {
            System.out.println("!=");
        }
    }
}
